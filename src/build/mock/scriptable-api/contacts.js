;(function () {
    function createContainer(name = 'iCloud') {
        return {
            identifier: `mock-container-${String(name).toLowerCase()}`,
            name,
        }
    }

    function createGroup(name = 'Friends') {
        return {
            identifier: `mock-group-${String(name).toLowerCase()}`,
            name,
        }
    }

    function createContact(givenName = 'Alex', familyName = 'Chen') {
        const fullName = `${givenName} ${familyName}`.trim()
        return {
            identifier: `mock-contact-${givenName.toLowerCase()}-${familyName.toLowerCase()}`,
            namePrefix: '',
            givenName,
            middleName: '',
            familyName,
            nickname: givenName,
            organizationName: 'Scriptable Playground',
            departmentName: 'Engineering',
            jobTitle: 'Widget Developer',
            phoneNumbers: [{label: 'mobile', value: '+1 415 555 0198'}],
            emailAddresses: [{label: 'work', value: `${givenName.toLowerCase()}.${familyName.toLowerCase()}@example.com`}],
            postalAddresses: [
                {
                    label: 'work',
                    street: '1 Apple Park Way',
                    city: 'Cupertino',
                    state: 'CA',
                    postalCode: '95014',
                    country: 'United States',
                },
            ],
            urlAddresses: [{label: 'homepage', value: 'https://scriptable.app'}],
            birthday: new Date(1990, 5, 18),
            note: 'Generated by Scriptable Playground.',
            image: null,
            fullName,
        }
    }

    const containers = [createContainer('iCloud')]
    const groups = [createGroup('Widget Team'), createGroup('Favorites')]
    const contacts = [createContact('Alex', 'Chen'), createContact('Taylor', 'Morgan')]

    ScriptableMock.register('Contacts', () => {
        class Contact {
            constructor() {
                Object.assign(this, createContact())
            }
            static async all() {
                return contacts.slice()
            }
            static async inGroups() {
                return contacts.slice(0, 1)
            }
            static async persistChanges() {
                return true
            }
            static add(contact) {
                contacts.push(contact)
            }
            static update(contact) {
                const index = contacts.findIndex(item => item.identifier === contact.identifier)
                if (index >= 0) contacts[index] = contact
            }
            static delete(contact) {
                const index = contacts.indexOf(contact)
                if (index >= 0) contacts.splice(index, 1)
            }
        }

        class ContactsContainer {
            static async default() {
                return containers[0]
            }
            static async all() {
                return containers.slice()
            }
            static async withIdentifier(identifier) {
                return containers.find(item => item.identifier === identifier) || containers[0]
            }
        }

        class ContactsGroup {
            constructor() {
                Object.assign(this, createGroup('New Group'))
            }
            static async all() {
                return groups.slice()
            }
            static add(group) {
                groups.push(group)
            }
            static update(group) {
                const index = groups.findIndex(item => item.identifier === group.identifier)
                if (index >= 0) groups[index] = group
            }
            static delete(group) {
                const index = groups.indexOf(group)
                if (index >= 0) groups.splice(index, 1)
            }
        }

        return {
            Contact,
            ContactsContainer,
            ContactsGroup,
            Contacts: {
                all: async () => contacts.slice(),
                presentPicker: async allowMultiple => (allowMultiple ? contacts.slice() : contacts[0]),
                inGroups: async () => contacts.slice(0, 1),
            },
        }
    })
})()
