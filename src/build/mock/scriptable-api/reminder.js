;(function () {
    function addDays(date, days) {
        return new Date(date.getFullYear(), date.getMonth(), date.getDate() + days, date.getHours(), date.getMinutes())
    }

    function createReminder(title = 'Review Playground mocks', dayOffset = 0, completed = false, priority = 0) {
        const now = new Date()
        const dueDate = new Date(now.getFullYear(), now.getMonth(), now.getDate() + dayOffset, 18, 0)
        const completionDate = completed ? addDays(dueDate, -1) : null
        return {
            identifier: `mock-reminder-${String(title).toLowerCase().replace(/\s+/g, '-')}`,
            title,
            notes: 'Generated by Scriptable Playground.',
            isCompleted: completed,
            isOverdue: !completed && dueDate.getTime() < now.getTime(),
            priority,
            dueDate,
            completionDate,
            creationDate: addDays(now, -2),
            calendar: ScriptableMock.createCalendar('Reminders'),
            addRecurrenceRule: ScriptableMock.noop,
            removeAllRecurrenceRules: ScriptableMock.noop,
            save: ScriptableMock.noop,
            remove: ScriptableMock.noop,
        }
    }

    function sampleReminders() {
        return [
            createReminder('Review Playground mocks', 0, false, 5),
            createReminder('Sync widget screenshots', 1, false, 3),
            createReminder('Archive old cache files', -1, true, 1),
            createReminder('Check API fallback data', 3, false, 4),
            createReminder('Ship previous mock cleanup', -8, true, 2),
        ]
    }

    function filterByDueRange(startDate, endDate, completed) {
        const start = startDate ? new Date(startDate).getTime() : -Infinity
        const end = endDate ? new Date(endDate).getTime() : Infinity
        return sampleReminders().filter(reminder => {
            const time = reminder.dueDate?.getTime?.() ?? 0
            const completionMatches = completed === undefined || reminder.isCompleted === completed
            return completionMatches && time >= start && time < end
        })
    }

    function dayRange(offset) {
        const now = new Date()
        const start = new Date(now.getFullYear(), now.getMonth(), now.getDate() + offset)
        const end = new Date(now.getFullYear(), now.getMonth(), now.getDate() + offset + 1)
        return [start, end]
    }

    function completedBetween(startDate, endDate) {
        const start = startDate ? new Date(startDate).getTime() : -Infinity
        const end = endDate ? new Date(endDate).getTime() : Infinity
        return sampleReminders().filter(reminder => {
            const time = reminder.completionDate?.getTime?.() ?? 0
            return reminder.isCompleted && time >= start && time < end
        })
    }

    ScriptableMock.register('Reminder', () => {
        class Reminder {
            constructor() {
                Object.assign(this, createReminder())
            }
            static async scheduled() {
                return sampleReminders().filter(reminder => !reminder.isCompleted)
            }
            static async all() {
                return sampleReminders()
            }
            static async allCompleted() {
                return sampleReminders().filter(reminder => reminder.isCompleted)
            }
            static async allIncomplete() {
                return sampleReminders().filter(reminder => !reminder.isCompleted)
            }
            static async allDueToday() {
                return filterByDueRange(...dayRange(0))
            }
            static async completedDueToday() {
                return filterByDueRange(...dayRange(0), true)
            }
            static async incompleteDueToday() {
                return filterByDueRange(...dayRange(0), false)
            }
            static async allDueTomorrow() {
                return filterByDueRange(...dayRange(1))
            }
            static async completedDueTomorrow() {
                return filterByDueRange(...dayRange(1), true)
            }
            static async incompleteDueTomorrow() {
                return filterByDueRange(...dayRange(1), false)
            }
            static async allDueYesterday() {
                return filterByDueRange(...dayRange(-1))
            }
            static async completedDueYesterday() {
                return filterByDueRange(...dayRange(-1), true)
            }
            static async incompleteDueYesterday() {
                return filterByDueRange(...dayRange(-1), false)
            }
            static async allDueThisWeek() {
                return filterByDueRange(new Date(), addDays(new Date(), 7))
            }
            static async completedDueThisWeek() {
                return filterByDueRange(new Date(), addDays(new Date(), 7), true)
            }
            static async incompleteDueThisWeek() {
                return filterByDueRange(new Date(), addDays(new Date(), 7), false)
            }
            static async allDueNextWeek() {
                return filterByDueRange(addDays(new Date(), 7), addDays(new Date(), 14))
            }
            static async completedDueNextWeek() {
                return filterByDueRange(addDays(new Date(), 7), addDays(new Date(), 14), true)
            }
            static async incompleteDueNextWeek() {
                return filterByDueRange(addDays(new Date(), 7), addDays(new Date(), 14), false)
            }
            static async allDueLastWeek() {
                return filterByDueRange(addDays(new Date(), -7), new Date())
            }
            static async completedDueLastWeek() {
                return filterByDueRange(addDays(new Date(), -7), new Date(), true)
            }
            static async incompleteDueLastWeek() {
                return filterByDueRange(addDays(new Date(), -7), new Date(), false)
            }
            static async allDueBetween(startDate, endDate) {
                return filterByDueRange(startDate, endDate)
            }
            static async completedDueBetween(startDate, endDate) {
                return filterByDueRange(startDate, endDate, true)
            }
            static async incompleteDueBetween(startDate, endDate) {
                return filterByDueRange(startDate, endDate, false)
            }
            static async completedToday() {
                return completedBetween(...dayRange(0))
            }
            static async completedThisWeek() {
                return completedBetween(new Date(), addDays(new Date(), 7))
            }
            static async completedLastWeek() {
                return completedBetween(addDays(new Date(), -7), new Date())
            }
            static async completedBetween(startDate, endDate) {
                return completedBetween(startDate, endDate)
            }
        }

        return {Reminder}
    })
})()
