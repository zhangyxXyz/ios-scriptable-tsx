;(function () {
    function addDays(date, days) {
        return new Date(date.getFullYear(), date.getMonth(), date.getDate() + days, date.getHours(), date.getMinutes())
    }

    function createCalendarEvent(title = 'Team Sync', dayOffset = 0, hour = 10, minute = 0, durationMinutes = 45) {
        const baseDate = addDays(new Date(), dayOffset)
        const startDate = new Date(baseDate.getFullYear(), baseDate.getMonth(), baseDate.getDate(), hour, minute)
        const endDate = new Date(startDate.getTime() + durationMinutes * 60 * 1000)
        return {
            identifier: `mock-event-${dayOffset}-${hour}-${minute}`,
            title,
            location: dayOffset === 0 ? 'Conference Room A' : 'FaceTime',
            notes: 'Generated by Scriptable Playground.',
            startDate,
            endDate,
            isAllDay: false,
            timeZone: Intl.DateTimeFormat().resolvedOptions().timeZone,
            calendar: ScriptableMock.createCalendar('Calendar'),
            addRecurrenceRule: ScriptableMock.noop,
            removeAllRecurrenceRules: ScriptableMock.noop,
            save: ScriptableMock.noop,
            remove: ScriptableMock.noop,
            presentEdit: async function () {
                return this
            },
        }
    }

    function sampleEvents() {
        return [
            createCalendarEvent('Morning standup', 0, 9, 30, 30),
            createCalendarEvent('Review widget preview', 0, 14, 0, 60),
            createCalendarEvent('Plan tomorrow', 1, 10, 0, 45),
            createCalendarEvent('Weekly summary', 5, 16, 30, 45),
            createCalendarEvent('Last week notes', -3, 11, 0, 30),
        ]
    }

    function eventsBetween(startDate, endDate) {
        const start = startDate ? new Date(startDate).getTime() : -Infinity
        const end = endDate ? new Date(endDate).getTime() : Infinity
        return sampleEvents().filter(event => event.startDate.getTime() >= start && event.startDate.getTime() <= end)
    }

    function dayRange(offset) {
        const now = new Date()
        const start = new Date(now.getFullYear(), now.getMonth(), now.getDate() + offset)
        const end = new Date(now.getFullYear(), now.getMonth(), now.getDate() + offset + 1)
        return [start, end]
    }

    ScriptableMock.register('CalendarEvent', () => {
        class CalendarEvent {
            constructor() {
                Object.assign(this, createCalendarEvent())
            }
            static async presentCreate() {
                return new CalendarEvent()
            }
            static async today() {
                return eventsBetween(...dayRange(0))
            }
            static async tomorrow() {
                return eventsBetween(...dayRange(1))
            }
            static async yesterday() {
                return eventsBetween(...dayRange(-1))
            }
            static async thisWeek() {
                const now = new Date()
                return eventsBetween(now, addDays(now, 7))
            }
            static async nextWeek() {
                const now = new Date()
                return eventsBetween(addDays(now, 7), addDays(now, 14))
            }
            static async lastWeek() {
                const now = new Date()
                return eventsBetween(addDays(now, -7), now)
            }
            static async between(startDate, endDate) {
                return eventsBetween(startDate, endDate)
            }
        }

        return {CalendarEvent}
    })
})()
